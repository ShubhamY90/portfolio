export default function CircularGallery({
  items, // This should be your projects array
  bend = 3,
  textColor = '#ffffff',
  borderRadius = 0.05,
  font = 'bold 30px Figtree',
  scrollSpeed = 2,
  scrollEase = 0.05
}) {
  const containerRef = useRef(null);
  const [modalOpen, setModalOpen] = useState(false);
  const [selectedImage, setSelectedImage] = useState(null);

  const handleImageClick = (imageData) => {
    setSelectedImage(imageData);
    setModalOpen(true);
  };

  const handleCloseModal = () => {
    setModalOpen(false);
    setSelectedImage(null);
  };

  useEffect(() => {
    const app = new App(containerRef.current, { 
      items, // Pass your projects array here
      bend, 
      textColor, 
      borderRadius, 
      font, 
      scrollSpeed, 
      scrollEase,
      onImageClick: handleImageClick
    });
    return () => {
      app.destroy();
    };
  }, [items, bend, textColor, borderRadius, font, scrollSpeed, scrollEase]);

  return (
    <>
      <div className="w-full h-full overflow-hidden cursor-grab active:cursor-grabbing" ref={containerRef} />
      <ImageModal 
        isOpen={modalOpen} 
        onClose={handleCloseModal} 
        imageData={selectedImage} 
        projects={items} // Pass projects array to modal for full data
      />
    </>
  );
}